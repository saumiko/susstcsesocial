public class loginid {
    public int id;
    public String username;
    public String name;
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    public String getUsername() {
        return username;
    }
    
    public void setUsername(String user) {
        this.username = user;
    }
    
    public String getName() {
        return name;
    }
    
    public void setName(String nam) {
        this.name = nam;
    }
}
